public class Excer_Q2 {
    public static void isEven(int n) {
        if (n % 2 == 0) {
            System.out.println("Your number"+n+" is Even :");
        }
        else {
              System.out.println("Your number " +n+" is Odd :" );
        }

    }
    public static void main(String[] args) {
        isEven(0);
    }
}
